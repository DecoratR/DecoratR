using System.Text;

namespace DecoratR.Generator;

internal static class SourceGenerationHelper
{
    internal static string GenerateHandlerAttribute() =>
        @"// <auto-generated />
#nullable enable

namespace DecoratR
{
    [global::System.AttributeUsage(global::System.AttributeTargets.Assembly)]
    internal sealed class GenerateHandlerRegistrationsAttribute : global::System.Attribute
    {
    }
}
";

    internal static string GenerateFullAttribute() =>
        @"// <auto-generated />
#nullable enable

namespace DecoratR
{
    /// <summary>
    /// Triggers the DecoratR source generator to discover all handlers and decorators in the compilation
    /// and emit a reflection-free <c>AddDecoratR()</c> extension method on <c>IServiceCollection</c>.
    /// Place this attribute in the composition root assembly (e.g. the web API project).
    /// </summary>
    [global::System.AttributeUsage(global::System.AttributeTargets.Assembly)]
    internal sealed class GenerateDecoratRRegistrationsAttribute : global::System.Attribute
    {
    }
}
";

    /// <summary>
    /// Generates the <c>HandlerRegistry</c> class (handler-only path, backward compatible).
    /// </summary>
    internal static string GenerateHandlerRegistry(string assemblyName, IReadOnlyList<HandlerMetadata> handlers)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine($"namespace {assemblyName}");
        sb.AppendLine("{");
        sb.AppendLine("    /// <summary>");
        sb.AppendLine($"    /// Compile-time discovered handler registrations for assembly '{assemblyName}'.");
        sb.AppendLine("    /// Generated by DecoratR.Generator.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    [global::System.CodeDom.Compiler.GeneratedCode(\"DecoratR.Generator\", \"1.0.0\")]");
        sb.AppendLine("    public static class HandlerRegistry");
        sb.AppendLine("    {");
        sb.AppendLine("        /// <summary>");
        sb.AppendLine("        /// All compile-time discovered handler registrations as (ServiceType, ImplementationType) tuples.");
        sb.AppendLine("        /// </summary>");
        sb.AppendLine("        public static global::System.Collections.Generic.List<(global::System.Type ServiceType, global::System.Type ImplementationType)> Handlers { get; } =");
        sb.AppendLine("        [");

        foreach (var handler in handlers)
        {
            sb.AppendLine($"            (typeof(global::DecoratR.IRequestHandler<{handler.RequestFullyQualifiedName}, {handler.ResponseFullyQualifiedName}>), typeof({handler.HandlerFullyQualifiedName})),");
        }

        sb.AppendLine("        ];");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    /// <summary>
    /// Generates the full registration class with <c>AddDecoratR()</c> extension,
    /// <c>HandlerRegistry</c>, and <c>DecoratorRegistry</c>.
    /// </summary>
    internal static string GenerateFullRegistrations(
        string assemblyName,
        IReadOnlyList<HandlerMetadata> handlers,
        IReadOnlyList<DecoratorMetadata> decorators)
    {
        var sb = new StringBuilder();

        sb.AppendLine("// <auto-generated />");
        sb.AppendLine("#nullable enable");
        sb.AppendLine();
        sb.AppendLine($"namespace {assemblyName}");
        sb.AppendLine("{");

        // ── AddDecoratR() extension method ────────────────────────────────────
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Reflection-free DecoratR registration generated by DecoratR.Generator.");
        sb.AppendLine("    /// Registers all discovered handlers and applies all discovered decorators.");
        sb.AppendLine("    /// Decorators are applied in alphabetical order (outermost first).");
        sb.AppendLine("    /// For custom decorator ordering, use the fluent API with <c>AddDecorator(typeof(...))</c> instead.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    [global::System.CodeDom.Compiler.GeneratedCode(\"DecoratR.Generator\", \"1.0.0\")]");
        sb.AppendLine("    public static class DecoratRServiceCollectionExtensions");
        sb.AppendLine("    {");
        sb.AppendLine("        public static global::Microsoft.Extensions.DependencyInjection.IServiceCollection AddDecoratR(");
        sb.AppendLine("            this global::Microsoft.Extensions.DependencyInjection.IServiceCollection services)");
        sb.AppendLine("        {");

        if (handlers.Count > 0)
        {
            sb.AppendLine("            // Register handlers");
            foreach (var handler in handlers)
            {
                sb.AppendLine($"            services.AddTransient<");
                sb.AppendLine($"                global::DecoratR.IRequestHandler<{handler.RequestFullyQualifiedName}, {handler.ResponseFullyQualifiedName}>,");
                sb.AppendLine($"                {handler.HandlerFullyQualifiedName}>();");
            }
        }

        if (decorators.Count > 0 && handlers.Count > 0)
        {
            sb.AppendLine();
            sb.AppendLine("            // Apply decorators to all handlers (alphabetical order, outermost first)");

            // Apply decorators: alphabetical order means first decorator registered = outermost.
            // The Decorate() method replaces the registration, so to get [A outer, B inner],
            // we apply in reverse: B first (innermost), then A (outermost wraps B).
            // But alphabetical order means A < B, so A is outermost → apply B first, then A.
            // i.e. iterate decorators in REVERSE alphabetical order for the Decorate() calls.
            var reversedDecorators = decorators.Reverse().ToList();

            foreach (var handler in handlers)
            {
                foreach (var decorator in reversedDecorators)
                {
                    sb.AppendLine($"            global::DecoratR.ServiceCollectionExtensions.Decorate(services,");
                    sb.AppendLine($"                typeof(global::DecoratR.IRequestHandler<{handler.RequestFullyQualifiedName}, {handler.ResponseFullyQualifiedName}>),");
                    sb.AppendLine($"                typeof({decorator.DecoratorFullyQualifiedName}<{handler.RequestFullyQualifiedName}, {handler.ResponseFullyQualifiedName}>));");
                }
            }
        }

        sb.AppendLine();
        sb.AppendLine("            return services;");
        sb.AppendLine("        }");
        sb.AppendLine("    }");
        sb.AppendLine();

        // ── HandlerRegistry (for manual use / custom ordering) ────────────────
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Compile-time discovered handler registrations. Use with the fluent API for custom ordering.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    [global::System.CodeDom.Compiler.GeneratedCode(\"DecoratR.Generator\", \"1.0.0\")]");
        sb.AppendLine("    public static class HandlerRegistry");
        sb.AppendLine("    {");
        sb.AppendLine("        public static global::System.Collections.Generic.List<(global::System.Type ServiceType, global::System.Type ImplementationType)> Handlers { get; } =");
        sb.AppendLine("        [");

        foreach (var handler in handlers)
        {
            sb.AppendLine($"            (typeof(global::DecoratR.IRequestHandler<{handler.RequestFullyQualifiedName}, {handler.ResponseFullyQualifiedName}>), typeof({handler.HandlerFullyQualifiedName})),");
        }

        sb.AppendLine("        ];");
        sb.AppendLine("    }");
        sb.AppendLine();

        // ── DecoratorRegistry (for manual use / custom ordering) ─────────────
        sb.AppendLine("    /// <summary>");
        sb.AppendLine("    /// Compile-time discovered open-generic decorator types. Use with <c>AddDecorator(typeof(...))</c> for custom ordering.");
        sb.AppendLine("    /// </summary>");
        sb.AppendLine("    [global::System.CodeDom.Compiler.GeneratedCode(\"DecoratR.Generator\", \"1.0.0\")]");
        sb.AppendLine("    public static class DecoratorRegistry");
        sb.AppendLine("    {");
        sb.AppendLine("        public static global::System.Collections.Generic.List<global::System.Type> Decorators { get; } =");
        sb.AppendLine("        [");

        foreach (var decorator in decorators)
        {
            // The open-generic typeof requires the backtick syntax at runtime
            // We need to use the type directly. Since we stripped the type params from the name,
            // we reference the open generic via its metadata: typeof(Foo<,>)
            sb.AppendLine($"            typeof({decorator.DecoratorFullyQualifiedName}<,>),");
        }

        sb.AppendLine("        ];");
        sb.AppendLine("    }");
        sb.AppendLine("}");

        return sb.ToString();
    }

    // Keep the old method name as an alias for backward compat with existing generator tests
    internal static string GenerateRegistrations(string assemblyName, IReadOnlyList<HandlerMetadata> handlers)
        => GenerateHandlerRegistry(assemblyName, handlers);
}
